let fizzBuzzArray = [];

// boucle for
for (let i = 0; i <= 100; i++) {
  fizzBuzzArray.push(i);
}
console.log(fizzBuzzArray);

// boucle while
let number = 0;
while (number <= 100) {
  fizzBuzzArray.push(number);
  number++;
}
console.log(fizzBuzzArray);

// boucle do-while

do {
  fizzBuzzArray.push(number);
  number++;
} while (number <= 100);
{
  console.log(fizzBuzzArray);
}
